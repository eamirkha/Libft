#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	k;
	unsigned char	*s1;

	i = 0;
	k = n;
	s1 = s;
	while (i != k)
	{
		s1[i] = '\0';
		i++;
	}
}
