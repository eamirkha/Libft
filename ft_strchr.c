#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	c1;
	char	*s1;

	i = 0;
	c1 = c;
	s1 = (char *)s;
	while (s1[i] != '\0')
	{
		if (s1[i] == c1)
		{
			return (&s1[i]);
		}
		i++;
	}
	if (s1[i] == c)
		return (&s1[i]);
	return (0);
}
